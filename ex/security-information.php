<h1>BrainScape - Security Information</h1><br>
<body>
<h4 style="display: inline;">1. Do NOT tell other players personal information.</h4>
<br><br>
Not even if they claim to be Jagex staff! The BrainScape game window blocks your password, but it is still a very bad idea to try typing it in, as you may mis-type it in such a way that it is still obvious.<br>
<ul style="margin-top: 0px; margin-bottom: 0px; ">
<br>
<li>Don't tell ANYONE your password, ever!<br><li>Don't tell others your password recovery-questions<br>
<li>Don't tell others your real name, email, ICQ number, credit-card details, etc...<br>
</ul>
<br><br>
<h4 style="display: inline;">2. Think before using your password.</h4>
<br><br>
Never enter your password or recovery-questions into ANY website, other than www.BrainScape.net or www.BrainScape.net/forums.
<br><br>
You should NOT use the same password at every website you visit; this is very dangerous! Your The BrainScape password should be different to all your other passwords, and ONLY used for The BrainScape.
<br><br>
When entering your password, make sure nobody is standing behind you watching you type it in! You wouldn't believe how often this happens! Please be careful. <br><br><br>
<h4 style="display: inline;">3. Watch out for fake websites!</h4>
<br><br>
There are websites which look very similar to www.BrainScape.net but <b>STEAL</b> your password. Don't trust a site just because it looks official.
<br>
<ul style="margin-top: 0px; margin-bottom: 0px; ">
<br>
<li>The BrainScape cheat sites - there are NO The BrainScape cheats. These are fakes!<br>
<li>The BrainScape staff sites - we do not have secret sites on which you can become an admin or moderator! These are fakes!<br>
<li>The BrainScape chat boards/rooms - don't use your The BrainScape password on these sites! Use a DIFFERENT password or you will regret it!<br>
<li>The BrainScape test servers - watch out for fake servers. If in doubt create a NEW account, rather than risking your main character.<br>
</ul>
<br><br>
<h4 style="display: inline;">4. Don't use unofficial The BrainScape add-ons.</h4>
<br><br>
There are programs which claim to make The BrainScape easier or better, but actually <b>STEAL</b> your password. Not only is using these programs extremely risky, it's also against the rules.
<br><br><br>
<h4 style="display: inline;">5. Watch out for fake emails, and fake staff!</h4>
<br><br>
There are many people pretending to be Jagex staff or impersonating its creators. It is easy to forge emails, so don't trust an email just because it looks official.<br><br><b>Real staff will NEVER ask you for your password!</b>
<br><br>
To help you be sure that correspondence from us is genuine, we send all messages to your Forum Messages. This is accessible from the Forums page, and will require your The BrainScape forums login details (again, please be sure that you are at www.BrainScape.net/forums).
<br><br>
We never send emails to players.<br>
<ul style="margin-top: 0px; margin-bottom: 0px; ">
<br>
<li>We <b>never</b> send emails saying we need your password to check you aren't cheating!<br>
<li>We <b>never</b> send emails saying we need your recovery-questions to check you didn't steal the account.<br>
<li>We <b>never</b> send emails containing attachments of any sort.<br>
<li>We <b>never</b> send emails saying we need your password to let you join the Staff Team.<br>
</ul>
<br><br>
<h4 style="display: inline;">6. Do NOT share your The BrainScape account with anyone else!</h4>
<br><br>
Not even your best friend! A large number of players have lost their account this way. Account sharing is against the rules and action will be taken against any players caught engaging in this.
<br><br><br>
<h4 style="display: inline;">7. Make sure your computer is secure!</h4>
<br><br>
Most windows distributions are NOT secure unless you take some basic precautions:<br>
<ul style="margin-top: 0px; margin-bottom: 0px; ">
<br>
<li>If you use windows get the latest updates and security fixes for your computer from <a href="http://windowsupdate.microsoft.com/" target="_blank">windowsupdate.microsoft.com</a> This is a <b>MUST</b>!<br>
<li>Install some up-to-date anti-virus software. Many people use Norton Antivirus. Alternatively, you can get a free virus scanner from <a href="http://www.grisoft.com/" target="_blank">www.grisoft.com</a><br>
<li>Install a firewall to protect your computer from external attacks. You can get free firewall software from <a href="http://www.zonealarm.com/" target="_blank">www.zonealarm.com</a><br>
<li>Be very careful about what programs you run to avoid a trojan being installed on your machine. For more information about trojans, and how to remove them see <a href="http://www.hackfix.org/" target="_blank">www.hackfix.org</a>.<br>
</ul>
<br><br>
<h4 style="display: inline;">8. Protecting your computer from spyware (unwanted advertising and tracking).</h4>
<br><br>
If, when you visit our website, you are seeing multiple pop-up adverts, they are unlikely to be coming from us. All our advertising partners have been given clear and strict instructions not to sell any form of pop-up adverts.
<br><br>
These pop-up adverts are probably coming from a program picked up from a third-party website, which spawns unwanted adverts whilst you are using the Internet. This type of program is common and known as 'spyware'.
<br><br>
There are free programs you can use to easily remove this 'spyware'.<br>
Ad-aware is a program from a reputable company called Lavasoft which can help keep your system clean. Ad-aware software is free and is available from their website at <a href="http://www.lavasoft.de/" target="_blank">www.lavasoft.de</a>.
