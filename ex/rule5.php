<h1>Rule 5 - Staff Impersonation</h1>
<body>
<br>
You should not attempt to impersonate BrainScape staff in any way, for any reason.<br>
<br>
The names of BrainScape Staff will have a gold crown next to their name when speaking in-game. Anyone who does not have a gold crown next to their name is not BrainScape staff. This applies to both private chat and general in game chat.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
We have this rule in place so our players can identify members of BrainScape staff and to ensure that players can trust members of our staff for help and support with any game related problems they may have.<br>
<br>
<h4 style="display: inline;">
Someone claiming to work for BrainScape has asked me for my account details. Should I give it to them?
</h4><br>
<br>
There are three ways to recognise BrainScape Staff:<br>
<ul style="margin-top: 0px; margin-bottom: 0px; list-style-type: decimal;">
<br>
<li>BrainScape Staff characters always have gold crowns next to their names<br>
</li>
<li>BrainScape Staff will never ask for a password.<br>
</li>
<li>BrainScape Staff will never contact you about your account in game.<br>
</li>
<li>BrainScape Staff will always be level 138(combat).<br>
</li>
</ul><br>
We have no 'undercover' staff, we have no need for your password to check your account, and we only communicate with players about account issues via their message center.<br>
<br>
If you ever see anyone attempting to impersonate a BrainScape Staff member or any other player then we would ask you to use the 'Report Abuse' button located at the bottom of the screen, and follow the on screen prompts so that we can take the appropriate actions against their account.<br>
<br>
<h4 style="display: inline;">
If BrainScape Staff have gold crowns, who are the players in-game that have silver crowns?
</h4><br>
<br>
The players that have silver crowns by their names when speaking in-game are Player Moderators. Anyone that does not have a silver crown next to their name is not a Player Moderator.<br>
<br>
For more information on Player Moderators please see the <a href="../ex/staff-center.php">Player Moderator</a> section.<br>
<br>
<i>To keep BrainScape enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
