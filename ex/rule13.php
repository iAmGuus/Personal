<h1>Rule 13 - Personal Details</h1>
<body>
<br>
For our players' privacy and safety, you must not ask for personal contact details such as full name, email address, instant messenger handles, telephone number or your home/school address. You also must not post anyone's personal contact details (including your own) on our in-game chat or forums.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
We have this rule in place to protect your privacy and ensure your safety. Not only may giving out your personal details affect the security of your account (as they may be used to make a false password recovery request), but it is also risky to tell people you meet over the internet your personal details.<br>
<br>
Please remember that you do not know any of these people in real life. This is not to say that they are not as nice in real life as they act in BrainScape, they most likely are, but it's important and best to keep a safe distance from your fellow BrainScape players.<br>
<br>
<h4 style="display: inline;">
What does personal details include?
</h4><br>
<br>
Personal details are considered to include full name, email addresses, instant messenger screen names/contact details, telephone numbers or your home/school address.<br>
<br>
<h4 style="display: inline;">
Is it all right if I ask my friend for their address?
</h4><br>
<br>
We do not recommend that you ask any other BrainScape players their personal details.<br>
<br>
Please be aware that you will meet many players. Therefore, please remember to keep safe in-game, and to read and follow our <a href="../ex/play-safe.php">Play Safely Guide</a>.<br>
<br>
<i>To keep BrainScape enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
