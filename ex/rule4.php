<h1>Rule 4 - Cheating/Bug Abuse</h1>
<body>
<br>
You must not use or attempt to use any cheats or errors which you find in our software.<br>
Any exploits which you find must be immediately reported to BrainScape through Customer Support.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
We have put a lot of effort into balancing the game to make it as fair and fun as possible.<br>
Unintended bugs can spoil the effect of the game, so we obviously want to fix them as quickly as possible. Deliberately taking advantage of a bug can unbalance the game economy or devalue other players' achievements, so it is against the rules.<br>
<br>
<h4 style="display: inline;">
What if I find a bug by accident? I don't want to get banned for accidentally discovering a bug!
</h4><br>
<br>
As long as you report the bug to us straight away, don't tell anyone else until we fix it, and don't use the bug any more, then you WON'T get in trouble. Indeed, we will be very grateful. If you don't tell us, or deliberately use it again and again, then you would be breaking the rule.<br>
<br>
<h4 style="display: inline;">
How do I report a bug?
</h4><br>
<br>
You can report a bug by going to the BrainScape Forums and look for the ''Report a Bug'' section.<br>
<br>
When filling out the bug report form please make sure you include as much detail as possible. This will help us find the bug and fix it as soon as possible. After the form has been filled out it will be sent to a member of BrainScape staff to investigate. Please note that BrainScape do not give rewards to players that report bugs.<br>
<br>
<h4 style="display: inline;">
Where can I view bugs that have already been reported?
</h4><br>
<br>
By going to the bug reporting system in the Customer Support section of the BrainScape website you will see a list of bugs that have already been confirmed. You will be able to see if the confirmed bugs are waiting to be fixed, fixed awaiting to be uploaded, or already fixed. Please note that if we feel that a bug will encourage bug abuse it will not be posted on this list. Please do not report bugs that are already on the confirmed bug list.<br>
<br>
<h4 style="display: inline;">
Are there any cheats for BrainScape?
</h4><br>
<br>
There are no cheat codes available for BrainScape, nor do we have any intention of adding any to the game. All gains have to be made fairly and via honest game play: any usage of cheats or bugs, or attempting to discover the existence of any cheats or bugs, is a direct violation of the Rules.<br>
<br>
Please do not send in requests for such information. If you do so, your account could be banned for attempted cheating and/or for Misuse of Customer Support.<br>
<br>
<i>To keep BrainScape enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
