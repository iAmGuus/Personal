<h1>Someone else knows my password, what should I do?</h1>
<body>
In this case we recommend that you first ensure that your computer is secure and that no-one has managed to get your password directly from your machine.<br><br>After that quickly manage to change your password ingame by typing ::changepassword (Your Password Here).<br>After you have done that they will no longer have axcess to your account.
</body>
<h1>What is a good password?</h1>
<body>
A good password is one that is not easy to guess or work out, is easy for you to remember, is at least eight letters long and contains a mix of letters and numbers.<br><br><b>DO NOT</b> pick any word or number which has a connection to you, so don't pick your favourite animal or your house number.<br><br><b>DO NOT</b> just use a number at the end of a word, such as HVYDG8076<br><br><b>DO NOT</b> use common number and letter substitutions e.g. 4 as the word 'for', 1 as 'L' or 'I', 5 as 'S'<br><br><b>DO NOT</b> use repeating characters 'bbbbbbbbbb' or series of characters such as 'kbkbkbkbkbkb'.<br><br><b>DO NOT</b> use your name, account name or name of an item either forwards, backwards or divided up.<br><br><b>DO NOT</b> use a series of characters off any keyboard such as 'qwerty', 'lkjhgf' or 'qazwsxedc', as these are very common and hijackers will look for these.<br><br>The only limitations placed on you by BrainScape are:<br><ul style="margin-top: 0px; margin-bottom: 0px; "><br><li>You can only have passwords up to 20 characters long.<br><br><li>You cannot use symbols such as '!' and '%'.<br></ul><br><br>Passwords are not case sensitive, so 'HVYDG' is the same as 'hvydg' or 'HvYdG'.<br><br>One way to make a password is to think up a collection of random alphabetical characters which do not exist as a word in any language. Then you should come up with a couple of random numbers which you can add to the word throughout. For example:<br><br>Word: GJWXREJUVGF<br><br>Number 1: 102<br><br>Number 2: 13<br><br>Combine them = G102JW13REJUVGF<br><br>This would be a very good password if it was not on this webpage. DO NOT use this password as any hijacker will be able to find this password and try it to open your account. Make your own up instead.<br><br>It is always worth having a notebook in which you can write down a copy of your password each time you change it so you can make sure you won't repeat any, and in case your password is hard to learn at first. You should of course keep this in a very safe place, like giving it to your parents or guardian for safekeeping.<br><br>As long as your password stays safe then there is no need to change your password often, as this just means you are more likely to reuse a password or forget the new one, but it is advisable to change it every 6 months or so.<br><br>It is especially important that if your account has been hijacked that you DO NOT use a password that you've used before on your account.
</body>
<h1>How do I change my password?</h1>
<body>
To change your password, please either go to BrainScape's main website page and click on the 'Change Your Password' link in the Manage Account section of the menu, down the left side of the screen or click <a href="#">here</a>.<br><br>You will then need to log-in, entering your username and password.<br><br>A page will then load which will allow you to change your password by confirming your current password and then entering the new one twice, to make sure there are no spelling mistakes.<br><br>Please remember that your password is only as secure as your computer and as secret as you keep it.
</body>
<h1>Should I keep my password secret?</h1>
<body>
We recommend that you do not tell <b>ANYONE</b> your password. To ensure that your account remains secure this includes your brothers or sisters and especially your friends. If someone tells you their BrainScape password, it is important that you tell them to change it as soon as possible as their account could be punished for Account Sharing.
</body>
<h1>How can I keep my password safe?</h1>
<body>
The best way to keep your password safe is to ensure that you keep it secret and that you keep your computer secure.<br><br>All users are advised to read our security tips.
</body>
