<h4 style="display: inline;">WARNING: READ BEFORE PLAYING</h4><br><br><h4 style="display: inline;">Photosensitive Seizure Warning</h4><br><br>A very small proportion of people can experience epileptic seizures when exposed to certain light patterns or flashing lights. Exposure to these patterns or backgrounds on a computer screen, and often while playing video games, may induce an epileptic seizure in these people. Certain conditions can induce previously undetected epileptic symptoms even in people who have no prior history of prior seizures or epilepsy.<br><br>If you, or anyone in your family, have an epileptic condition, consult your physician prior to playing BrainScape. If you experience any of the following symptoms while playing a video or computer game - dizziness, eye or muscle twitches, altered vision, loss of awareness, disorientation, involuntary movement, or convulsions - STOP PLAYING IMMEDIATELY and consult your physician. Only when they have given you the all clear should you resume play.<br><br><h4 style="display: inline;">Repetitive Strain Injury Warning</h4><br><br>Prolonged use of a computer, bad posture and low levels of fitness can all contribute to a condition referred to as Repetitive Strain Injury (RSI).<br><br>To help avoid RSI, please follow these simple steps:<br><br>When playing BrainScape take a 10 minute break from your computer every hour. Get up and walk around, do some stretches. Exercise regularly. When using the computer, only maintain a loose grip on the mouse. Never rest your wrists on a support whilst typing or using the mouse. For extended periods of mouse-only use, place the mouse directly in front of you, between you and the monitor. Make sure the top of the monitor is at eye level and directly in front of you and that your seat is at the right height so that your forearms are at right angles to your upper arms.<br><br>The picture below illustrates a correct sitting position:<br><br><img style="display: inline;display: block;