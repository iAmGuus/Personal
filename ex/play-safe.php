<h1>Play Safely</h1>
<body>
BrainScape is a multiplayer adventure so, let's face it, you are going to meet lots of new and exciting friends along your travels.<br><br>
However, please remember you <b>DO NOT</b> know any of these people in real-life. This is not to say that they are not as nice in real-life as they act in BrainScape, they most likely are, but it's important and best to keep a safe distance from your fellow BrainScape players in real-life.<br><br>
For this reason, we have put together some helpful hints so you can be both safe and have fun in BrainScape.<br><br>
1. <b>DO NOT</b> tell other players personal information. Not even if they claim to be Jagex staff!<br><br>Don't tell <b>ANYONE</b> your real name, email address, home address, messenger handle or phone number. Use your BrainScape in-game friends list to chat and interact with the other players safely.<br><br>
2. If someone is making you feel uncomfortable then you should not attempt to carry on a conversation with this person.<br><br>
3. BrainScape is an <b>ONLINE</b> game. There is no need to meet anyone from BrainScape in real life. Remember, you only have the other player's word that they are who they say they are. In addition, for our players' safety, it is against the rules to ask for or give out personal information, so please do not do so. For further information about this policy, please read <a href="#">Rule 13</a>.<br><br>If you decide against this and do plan to meet a BrainScape player in real life, please inform your parents or guardian what you are doing and have them accompany you to the meeting. <b>DO NOT</b> go alone.<br><br>
4. Always keep your parents or guardians informed of how your game is going. It would be a good idea to introduce them to the game and encourage them to check on you regularly to ensure that your game remains problem-free.<br><br>
5. If you need any help we are only a website form away. Please contact Customer Support and we will get back to you <b>ASAP</b>. Have fun and be safe. That's what BrainScape is all about!
